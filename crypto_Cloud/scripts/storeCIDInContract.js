// Function to store the CID in the contract
// Function to store the CID in the contract and then open the browser


async function storeCIDAndOpenBrowser(cid) {
  // This should match the function in your contract for storing a CID
  const tx = await contract.addDocument(cid);


  await tx.wait();
  console.log(`CID stored in contract: ${tx.hash}`);

  // Open the web browser with the IPFS URL
  const url = `http://localhost:8080/ipfs/${cid}`;
  open(url).catch(console.error);
}

